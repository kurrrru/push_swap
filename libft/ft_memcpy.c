#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	size_t	i;

	if (!dst && !src)
		return (NULL);
	i = 0;
	while (++i <= n)
		((char *)dst)[i - 1] = ((char *)src)[i - 1];
	return (dst);
}
